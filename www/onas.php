<div class='postheader'><div class='postheaderNadpis'><h1>O nás</h1></div></div>
<div class="postcontent">
	<p>Tým, který nelze popsat ani tisícem slov. Tým plný jak skvělých, jedinečných a 
  vždy ledově chladných individualit tak také plný nepřekonatelného až téměř mytického 
  týmového ducha. Tento tým vznikl v horkých letních dnech roku 2008 a od počátku si dal 
  za cíl jediné: Vítězit! Od prvních nesmělých společných dotyků s děrovaným míčkem bylo 
  jasné, že vstup do 2. Ligy RAFLu je pro tento tým velkým podceněním kvalit. Ale to nic 
  neměnilo na srdceryvnosti a obětavosti všech hráčů v každé minutě a vteřině všech 
  odehraných zápasů. Sezóna 2008/2009 začala až téměř hrozivým vítězstvím 32:6! Vikingové 
  poté dostáli svého jména a ničili všechny soupeře a skóre se ubíralo mílovými skoky 
  kupředu. Jediným (a jak se později ukázalo tak také osudovým) statečně odolávajícím 
  soupeřem byl tým Dr. Aci Veselá, který dokázal připravit Vikingy o jejich nepřetržitou 
  řadu vítězství. Tak tedy s jediným malým šrámem ve výsledcích Vikingové podle očekávání 
  postoupili do vyřazovacích bojů. Ukolébáni a opojeni předchozími výsledky se Vikingové 
  trápili v 2. kole play-off s velmi oslabenými hráči Benbro a po dvou vydřených vítězstvích 
  se probojovali do baráže o vysněnou 1. Ligu RAFLu! Tým Vikingů se na začátku baráže 
  potýkal se zraněními, ale o to bylo cennější první a velmi důležité vítězství nad 
  prvoligovými FBC Young Skill. Následovala i výhra nad dalším týmem první ligy, FBK SUP,
  ale pak se stalo něco, o čem se nikomu z Vikingů nezdálo 
  ani v té nejhorší noční můře! První prohra! Po velmi špatném výkonu byli Vikingové 
  poraženi druholigovými a velmi dobře známými Dr. Aci Veselá o jednu jedinou branku. 
  A aby tento černý den v historii týmu Fbc Vikings byl ještě černější, tak tento 
  barážový zápas byl zároveň finálovým zápasem druhé ligy. A výsledek?! Bez jediné 
  prohry v základní části se tedy tým umístil až na velmi smutném 2. místě. Toto zklamání 
  však neodradilo od oslavení postupu do 1. ligy!
  <br />
  <br />
  A teď už jen nedočkavě čekáme na ročník 2009/2010 a tajně doufáme, že i v 1. lize 
  bude soupeře při vyslovení Fbc Vikings mrazit v zádech.
  <br />
  <br />
  První dva roky v 1. lize nebyly pro FBC Vikings vůbec snadné. Vikingové bojovali namísto 
  se soupeři sami se sebou. Tým se potýkal s častou obměnou mužstva a následnou nesehraností, 
  s nezájmem spoluhráčů o hru a trénink a se zraněními. Po dvou letech ale konečně FBC Vikings 
  dostávají svému jménu, drtí spoluhráče a derou se na přední příčky 1. ligy. Držme jim palce, 
  ať jim to vydrží co nejdéle. 
</p>
